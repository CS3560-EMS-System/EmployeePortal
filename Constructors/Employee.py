class Employee:
    def __init__(self, employee_ID, firstName, lastName, email, DOB, phone, address, roleID, status):
        self.employee_ID = employee_ID
        self.firstName = firstName
        self.lastName = lastName
        self.email = email
        self.DOB = DOB
        self.phone = phone
        self.address = address
        self.roleID = roleID
        self.status = status
